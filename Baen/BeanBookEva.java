package cn.edu.zucc.booklib.model;

public class BeanBookEva {
	private String evaid;
	private String barcode;
	private String userid;
	private String evades;
	private String viewtag;
	private String liketag;
	public String getViewtag() {
		return viewtag;
	}
	public void setViewtag(String viewtag) {
		this.viewtag = viewtag;
	}
	public String getLiketag() {
		return liketag;
	}
	public void setLiketag(String liketag) {
		this.liketag = liketag;
	}
	public String getEvaid() {
		return evaid;
	}
	public void setEvaid(String evaid) {
		this.evaid = evaid;
	}
	public String getBarcode() {
		return barcode;
	}
	public void setBarcode(String barcode) {
		this.barcode = barcode;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getEvades() {
		return evades;
	}
	public void setEvades(String evades) {
		this.evades = evades;
	}
	
	
}
