package cn.edu.zucc.booklib.model;

public class BeanBookReader {
	private String bookreaderid;
	private String barcode;
	private String readerid;
	private int brnum;
	public String getBookreaderid() {
		return bookreaderid;
	}
	public void setBookreaderid(String bookreaderid) {
		this.bookreaderid = bookreaderid;
	}
	public String getBarcode() {
		return barcode;
	}
	public void setBarcode(String barcode) {
		this.barcode = barcode;
	}
	public String getReaderid() {
		return readerid;
	}
	public void setReaderid(String readerid) {
		this.readerid = readerid;
	}
	public int getBrnum() {
		return brnum;
	}
	public void setBrnum(int brnum) {
		this.brnum = brnum;
	}
	
}
